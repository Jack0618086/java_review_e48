public class Brad01 {

	public static void main(String[] args) {
		// 整數: byte: 2^8 (-128 ~ 127), short: 2^16, int: 2^32, long: 2^64
		// 含正負
		byte var1 = 127;	// 變數名稱: 
		System.out.println(var1);
		
		byte $_$ = 100;
		System.out.println($_$);
		byte True = 12;
		System.out.println(True);
		
	}

}
